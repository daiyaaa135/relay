/**
 * Returns the list of known aliases for a brand+model (e.g. for "Samsung", "Galaxy S25 Ultra").
 * Key is normalized as "Brand|Model" (trimmed, as stored).
 */
export function getAliasesForModel(brand: string, model: string): string[] {
  const key = `${(brand ?? '').trim()}|${(model ?? '').trim()}`;
  return MODEL_ALIASES[key] ?? [];
}

/**
 * Returns true if the IMEIDB alias (e.g. SM-S938U, XT2519-1) is a known alias for the user's selected brand+model.
 */
export function isAliasForModel(
  brand: string,
  model: string,
  imeidbAlias: string | undefined
): boolean {
  if (!imeidbAlias?.trim()) return false;
  const aliases = getAliasesForModel(brand, model);
  const normalized = imeidbAlias.trim();
  return aliases.some((a) => a.trim() === normalized);
}
